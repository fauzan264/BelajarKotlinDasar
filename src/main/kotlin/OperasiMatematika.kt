fun main(){
    var result1: Double = 10.0/3.0
    println(result1)

    var result2 = 10+10/2
    println(result2)

    // Augmented Assignments
    var total = 0

    val barang1 = 100
    total+= barang1

    val barang2 = 200
    total+= barang2

    val barang3 = 300
    total+= barang3

    println(total)

    // Unary operator
    total++
    total++
    total++
    total++
    total++

    println(total)

    val sehat = true
    println(!sehat)
}